void main(List<String> args) {
  double age = 24;

  age += 1; //Here age +=1 mean age = age + 1;
  print(age);

  age -= 1; //Here age -=1 mean age = age - 1;
  print(age);

  age *= 2; //Here age *=2 mean age = age * 2;
  print(age);

  age /= 2; //Here age *=2 mean age = age * 2;
  print(age);
}
